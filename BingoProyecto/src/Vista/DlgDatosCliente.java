    // ðŸ‘‰ Rellenar los datos del cliente
    public void setDatosCliente(Persona p) {
        if (p == null) {
            txtNombreUsuario.setText("");
            txtApellidoUsuario.setText("");
            txtCedulaUsuario.setText("");
            return;
        }

        txtNombreUsuario.setText(p.getNombre());
        txtApellidoUsuario.setText(p.getApellidos());
        txtCedulaUsuario.setText(p.getCedula());
    }

